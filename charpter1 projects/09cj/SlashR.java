//Slashr.java(beginning return words)
class Slashr{
public static void main(String[] args){
System.out.println("abcdf\rvna"); //vnadf
System.out.println("abcdf\rvnajkkjlk");//vanjkkjlk
System.out.println("abcdf\r\r\rghj"); //ghjdf
System.out.println("anand\rVy");//vyand
System.out.println("anand\rvyshu");//vyshu
System.out.println("anan\rvy");//vyan
System.out.println("hhfkdhgood\ranand");//anandhgood
}
}